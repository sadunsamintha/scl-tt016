package com.sicpa.standard.sasscl.devices.config;

import com.sicpa.standard.client.common.descriptor.DescriptorWrapper;

public class SASSCLDescriptorWrapper extends DescriptorWrapper {

	protected String id;

	public SASSCLDescriptorWrapper() {
	}

	public SASSCLDescriptorWrapper(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((this.id == null) ? 0 : this.id.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		SASSCLDescriptorWrapper other = (SASSCLDescriptorWrapper) obj;
		if (this.id == null) {
			if (other.id != null) {
				return false;
			}
		} else if (!this.id.equals(other.id)) {
			return false;
		}
		return true;
	}

}
